
/* this function checks for the winner and returns 'X' , 'O' or null  */
export function calculateWinner(squares) {
    const lines = [    //different possibilities of wins
        [0, 1, 2],
        [3, 4, 5],
        [6, 7, 8],
        [0, 3, 6],
        [1, 4, 7],
        [2, 5, 8],
        [0, 4, 8],
        [2, 4, 6]
    ];

    /* this loop checks the combinations in the squares with the winning combinations in the array */
    for(let i = 0; i < lines.length; i++) {
        const [a, b, c] = lines[i];
        if (squares[a] && squares[a] === squares[b] && squares[a] === squares[c]) {
            return squares[a];
        }
    }
    return null;
}